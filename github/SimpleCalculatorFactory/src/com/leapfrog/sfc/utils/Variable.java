package com.leapfrog.sfc.utils;

/**
 *
 * @author Anonymous
 */
public abstract class Variable {

    private String result;

    public Variable() {
    }

    public Variable(String result) {
        this.result = result;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

  
}